# 将整数转换为整数 (Converting an integer to an integer)
print(int(1))

# 将浮点数转换为整数（小数部分被截断） (Converting a float to an integer - decimal part is truncated)
print(int(1.1))

# 将字符串转换为整数 (Converting a string to an integer)
print(int("1"))

# 将布尔值True转换为整数（True对应1） (Converting boolean True to an integer - True corresponds to 1)
print(int(True))

# 将布尔值False转换为整数（False对应0） (Converting boolean False to an integer - False corresponds to 0)
print(int(False))

# 将浮点数转换为整数（向下取整到0） (Converting a float to an integer - rounded down to 0)
print(int(0.99))

# 将浮点数转换为整数（向下取整到0） (Converting a float to an integer - rounded down to 0)
print(int(0.01))

# 将整数转换为字符串 (Converting an integer to a string)
print(str(1))

# 将浮点数转换为字符串 (Converting a float to a string)
print(str(1.1))

# 将布尔值转换为字符串 (Converting a boolean to a string)
print(str(True))

# 将浮点数转换为字符串（小数部分被保留） (Converting a float to a string - decimal part is preserved)
print(str(1.9))

# 将整数转换为浮点数 (Converting an integer to a float)
print(float(1))

# 将列表转换为字符串 (Converting a list to a string)
print([1, 2, 3])

# 将字符串转换为列表 (Converting a string to a list)
print(list("123"))

# 将字符串转换为元组 (Converting a string to a tuple)
print(tuple("123"))

# 将字符串转换为字典 (Converting a string to a dictionary)
print(dict({"1": 1, "2": 2, "3": 3}))

# 将字符串转换为集合 (Converting a string to a set)
print(set("123"))

# 将字符串转换为布尔值 (Converting a string to a boolean)
print(bool("True"))

# 将字符串转换为浮点数 (Converting a string to a float)
print(float("1.1"))

# 将整数转换为字符 (Converting an integer to a character)
print(chr(97))

# 将字符转换为整数 (Converting a character to an integer)
print(ord('a'))
